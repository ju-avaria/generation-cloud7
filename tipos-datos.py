print("Python has three numeric types: int, float, and complex")

miValor = 1
print(miValor)
print(type(miValor))

print(str(miValor) + " es de tipo " + str(type(miValor)))

miValor = 3.14
print(miValor)
print(type(miValor))
print(str(miValor) + " es de tipo " + str(type(miValor)))

miValor = 5j
print(miValor)
print(type(miValor))
print(str(miValor) + " es de tipo " + str(type(miValor)))

miValor = True
print(miValor)  
print(type(miValor))
print(str(miValor) + " es de tipo " + str(type(miValor)))

miValor = False
print(miValor)
print(type(miValor))
print(str(miValor) + " es de tipo " + str(type(miValor)))